let currentLang = 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('currentLang', lang);
    applyTranslations();
    updateRTLStatus();
}

function applyTranslations() {
    document.querySelectorAll('[data-lang-key]').forEach(element => {
        const key = element.getAttribute('data-lang-key');
        const keys = key.split('.');
        let text = translations[currentLang];
        for (let i = 0; i < keys.length; i++) {
            if (text && text[keys[i]]) {
                text = text[keys[i]];
            } else {
                text = ''; // Fallback if key not found
                break;
            }
        }
        element.textContent = text;
    });

    // Update package items for trips section
    const packageItemsContainer = document.querySelector('.package-items-grid');
    if (packageItemsContainer) {
        packageItemsContainer.innerHTML = ''; // Clear existing items
        translations[currentLang].trip.packageItems.forEach(item => {
            const div = document.createElement('div');
            div.classList.add('package-item');
            div.innerHTML = `<i class="fas fa-check"></i><span>${item}</span>`;
            packageItemsContainer.appendChild(div);
        });
    }
}

function updateRTLStatus() {
    if (currentLang === 'ar') {
        document.body.classList.add('rtl');
    } else {
        document.body.classList.remove('rtl');
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('currentLang');
    if (savedLang) {
        setLanguage(savedLang);
    } else {
        setLanguage('en'); // Default to English
    }

    // Language switcher event listeners
    document.querySelectorAll('.lang-dropdown a').forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = e.target.getAttribute('data-lang');
            setLanguage(lang);
            document.querySelector('.lang-button').textContent = languages[lang].name; // Update button text
        });
    });
});


